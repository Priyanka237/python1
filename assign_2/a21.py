a = int(input("enter the no. a:"))
b = int(input("enter the no. b:"))
ch = input("enter your choice:")
while ch:
    if ch == '+':
        print('sum is:',a + b)
        break
    elif ch == '-':
        print('sub is:',a - b)
        break
    elif ch == '*':
        print('mul is:',a * b)  
        break   
    elif ch == '/':
        print('div is:',a / b)
        break
    elif ch == '%':
        print('mod is:',a % b)
        break
    else:
        print('nothing')
        break
        


